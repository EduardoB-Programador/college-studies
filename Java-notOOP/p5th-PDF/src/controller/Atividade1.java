/**Português
 * Desenvolver um programa que exiba os números de 1 a 100.
 *
 * English
 * Create a code that shows the numbers from 1 to 100.
 **/

public class Atividade1 {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 100) {
            System.out.println(i);
            i++;
        }
    }
}
